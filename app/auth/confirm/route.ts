import { createClient } from '@/lib/supabase/server'
import { NextResponse } from 'next/server'
import { type EmailOtpType } from '@supabase/supabase-js'

export async function GET(request: Request) {
  const { searchParams } = new URL(request.url)
  const token_hash = searchParams.get('token_hash')
  const type = searchParams.get('type') as EmailOtpType | null
  const next = searchParams.get('next') ?? '/'

  if (token_hash && type) {
    const supabase = await createClient()

    const { error } = await supabase.auth.verifyOtp({
      type,
      token_hash,
    })

    if (!error) {
      // Email confirmed successfully
      // Redirect to form for new users, or to the requested page
      return NextResponse.redirect(new URL('/form', request.url))
    }
  }

  // Redirect to error page or home on failure
  return NextResponse.redirect(new URL('/?error=confirmation_failed', request.url))
}